#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*out;

	i = 0;
	if (s == NULL || f == NULL)
		return (NULL);
	out = (char *)malloc(ft_strlen(s) * sizeof(char) + 1);
	if (!out)
		return (NULL);
	while (s[i])
	{
		out[i] = f(i, s[i]);
		i++;
	}
	out[i] = '\0';
	return (out);
}
